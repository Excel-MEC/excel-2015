Defuse is a digital circuit analysis and debugging event. In a time bound scenario, the event asks its contestants to figure out how to disarm a ticking bomb.<br>
The event is comprised of 3 rounds following a Preliminary Round. Teams of three or less can participate in the event.

<br><br><b>Preliminary Round</b>
<br>The Preliminary Round is multiple choice question type, with 30 questions and 30 minutes. 10 teams clearing the Prelims will move on to Round 1.

<br><br><b>Round 1</b>
<br>Round 1 is a 30 minute back to back circuit challenge where the participants may be asked to analyze and debug digital circuits, retrieve sequences or find the outputs of a maximum of 10 ‘bombing’ related questions.  6 teams clearing Round 1 move ahead to Round 2

<br><br><b>Round 2</b>
<br>Round 2 is a 30 minute round where the participating teams are expected to analyse and find a solution to a bombing related circuit.  4 teams making the cut will compete in the Final Round.

<br><br><b>Final Round</b>
<br>The Final Round is a race to defuse the bomb. The teams must, in 60 minutes or less analyze the circuitry, and find a way to deactivate the device. The first team to do so is awarded the first prize.

